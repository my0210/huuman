type TranslationKey = keyof typeof strings;

function resolveBase(code: LanguageCode): string {
  return code.split("-")[0];
}

export function t(key: TranslationKey, lang: LanguageCode): string {
  const entry = strings[key];
  if (!entry) return key;

  const exact = (entry as Record<string, string>)[lang];
  if (exact) return exact;

  const base = resolveBase(lang);
  const baseMatch = (entry as Record<string, string>)[base];
  if (baseMatch) return baseMatch;

  return (entry as Record<string, string>)["en"] ?? key;
}
